namespace ShopDemo.Auth.Infra.Data.Repositories;

public sealed class RoleClaimRepository
    : RepositoryBase<RoleClaim>,
    IRoleClaimRepository
{
    private readonly IRoleClaimPostgreSqlRepository _postgreSqlRepository;

    public RoleClaimRepository(
        ILogger<RoleClaimRepository> logger,
        IRoleClaimPostgreSqlRepository postgreSqlRepository
    ) : base(logger)
    {
        ArgumentNullException.ThrowIfNull(postgreSqlRepository);
        _postgreSqlRepository = postgreSqlRepository;
    }

